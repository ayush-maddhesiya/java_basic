public class AssigmentOpertors {
    public static void main(String[] args) {
        int p = 10;
        int q ;
        int r = 5;

        // =
        q = p;
        // += 
        q += p;
        System.out.println(q);
        // -=
        q -= p;
        System.out.println(q);

        // /= 
        p /= q;
        System.out.println(p);

        // *=
        q*=r;
        System.out.println(q);
    }
}
